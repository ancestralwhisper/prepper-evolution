// ─── Helper: Get unique makes ──────────────────────────────────────

export function getUniqueMakes(): string[] {
  return Array.from(new Set(vehicleDatabase.map((v) => v.make))).sort();
}

export function getModelsForMake(make: string): string[] {
  return Array.from(new Set(vehicleDatabase.filter((v) => v.make === make).map((v) => v.model))).sort();
}

export function getTrimsForModel(make: string, model: string): string[] {
  return vehicleDatabase
    .filter((v) => v.make === make && v.model === model)
    .map((v) => `${v.year} ${v.trim}`)
    .sort();
}

export function findVehicle(make: string, model: string, yearTrim: string): StockVehicle | undefined {
  const year = parseInt(yearTrim);
  const trim = yearTrim.replace(/^\d+\s*/, "");
  return vehicleDatabase.find(
    (v) => v.make === make && v.model === model && v.year === year && v.trim === trim,
  );
}