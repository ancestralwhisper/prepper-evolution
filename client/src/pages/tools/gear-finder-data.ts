export function getGearByCategory(category: GearCategory): GearProduct[] {
  return allGear.filter((g) => g.category === category);
}

// ─── Sort helpers ────────────────────────────────────────────────────────────
export type GearSortKey = "lightest" | "heaviest" | "cheapest" | "expensive" | "value";

export function sortGear(list: GearProduct[], key: GearSortKey): GearProduct[] {
  const sorted = [...list];
  switch (key) {
    case "lightest":
      return sorted.sort((a, b) => a.weightOz - b.weightOz);
    case "heaviest":
      return sorted.sort((a, b) => b.weightOz - a.weightOz);
    case "cheapest":
      return sorted.sort((a, b) => a.price - b.price);
    case "expensive":
      return sorted.sort((a, b) => b.price - a.price);
    case "value":
      return sorted.sort((a, b) => a.price / a.weightOz - b.price / b.weightOz);
  }
}

